<div class="cheading1">
<h4>What you'll learn </h4>

<p>
• Grasp how Javascript works and it's fundamental concepts <br>
• Write solid, good Javascript code <br>
• Understand advanced concepts such as closures, prototypal inheritance, IIFEs, and much more. <br>
• Drastically improve your ability to debug problems in Javascript. <br>
• Avoid common pitfalls and mistakes other Javascript coders make<br>
• Understand the source code of popular Javascript frameworks<br>
• Build your own Javascript framework or library<br>


</p>

<h4>Requirements</h4>
<p>•  Basic Javascript knowledge (variables, loops, and basic functions - that's all the course expects you to know!) <br>
• A browser and text editor
</p>

<h4>Description</h4>
<p>Javascript is the language that modern developers need to know, and know well. <br> Truly knowing Javascript will get you a job, and enable you to build quality web and server applications. <br>
<b>NOTE: This course includes information on ECMAScript 6 (ES6) the next version of Javascript! </b> <br><br>
In this course you will gain a deep understanding of Javascript, learn how Javascript works under the hood, <br>and how that knowledge helps you avoid common pitfalls and drastically improve your ability to debug problems. <br>You will find clarity in the parts that others, even experienced coders, may find weird, odd, and at times incomprehensible.<br> You'll learn the beauty and deceptive power of this language that is at the forefront of modern software development today.<br>
