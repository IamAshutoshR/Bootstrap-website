<h4>What you'll learn </h4>

<p>
• Build artificial neural networks with Tensorflow and Keras<br>
• Classify images, data, and sentiments using deep learning<br>
• Make predictions using linear regression, polynomial regression, and multivariate regression<br>
• Data Visualization with MatPlotLib and Seaborn<br>
• Implement machine learning at massive scale with Apache Spark's MLLib<br>
• Understand reinforcement learning - and how to build a Pac-Man bot<br>
• Classify data using K-Means clustering, Support Vector Machines (SVM), KNN, Decision Trees, Naive Bayes, and PCA<br>
• Use train/test and K-Fold cross validation to choose and tune your models<br>
• Build a movie recommender system using item-based and user-based collaborative filtering<br>
• Clean your input data to remove outliers<br>
• Design and evaluate A/B tests using T-Tests and P-Values<br>



