<h4>Description</h4>
<p>• Which programming language is often seen as a badge of honor among software developers?  C++  <br>

• Which programming language can you learn that when added to your resume,  will often get you a job interview?  C++ <br>

•  Which programming language is routinely ranked in the top 5 programming languages by popularity, and been consistently in the top 10 for close to 20 years?  C++</p>

<h4>Why you should learn C++ 
</h4>
<p>• Much, if not most of the software written today is still written in C++ and this has been the case for many, many years. <br>
• Not only is C++ popular, but it is also a very relevant language. If you go to GitHub you will see that there are a huge number of active C++ repositories and C++ is also extremely active on stack overflow. <br>
• There are many, many leading software titles written entirely or partly in C++. These include the Windows, Linux, and Mac OSX operating systems! <br>
• Many of the Adobe products such as Photoshop and Illustrator,  the MySQL and MongoDB database engines, and many many more are written in C++. <br>
• Leading tech companies use C++ for many of their products and internal research and development. These include Amazon, Apple, Microsoft, PayPal, Google, Facebook, Oracle, and many more.</p>

